#Create a program that asks the user to enter two numbers and then prints out the sum, difference, product, and quotient of those numbers
num1 = input ("enter first number: ")
num2 = input ("Enter a second number: ")
print ("The Sum of these numbers is: ", float(num1)+float(num2))
print ("The product of these numbers is: ", (float(num1)*float(num2)))
print ("The difference between these numbers is: ", float(num1)-float(num2))
print ("The quotient of these numbers is: ", (float(num1)/float(num2)))
